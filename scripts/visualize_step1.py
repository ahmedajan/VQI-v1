def plot_dprime_forest(scores, impostors):
    """Forest plot of d-prime with bootstrap 95% CI."""
    print("  1.6_scores: score_dprime_forest_plot.png...")

    fig, ax = plt.subplots(figsize=(8, 5))
    y_positions = np.arange(len(PROVIDERS) * 2)
    labels = []
    centers = []
    ci_low = []
    ci_high = []
    colors_list = []

    for pn in PROVIDERS:
        gen_raw = scores[pn]["genuine_raw"].dropna().values
        gen_norm = scores[pn]["genuine_norm"].dropna().values
        imp_raw = impostors[pn]["raw"]
        imp_norm = impostors[pn]["norm"]

        # Bootstrap d-prime (raw)
        rng = np.random.RandomState(42)
        n_boot = 1000
        dp_boots_raw = []
        dp_boots_norm = []
        for _ in range(n_boot):
            g_s = rng.choice(gen_raw, len(gen_raw), replace=True)
            i_s = rng.choice(imp_raw, min(100000, len(imp_raw)), replace=True)
            dp_boots_raw.append(compute_dprime(g_s, i_s))
            g_sn = rng.choice(gen_norm, len(gen_norm), replace=True)
            i_sn = rng.choice(imp_norm, min(100000, len(imp_norm)), replace=True)
            dp_boots_norm.append(compute_dprime(g_sn, i_sn))

        dp_raw = compute_dprime(gen_raw, imp_raw)
        dp_norm = compute_dprime(gen_norm, imp_norm)

        labels.append(f"{pn} (Raw)")
        centers.append(dp_raw)
        ci_low.append(np.percentile(dp_boots_raw, 2.5))
        ci_high.append(np.percentile(dp_boots_raw, 97.5))
        colors_list.append(COLORS[pn])

        labels.append(f"{pn} (S-Norm)")
        centers.append(dp_norm)
        ci_low.append(np.percentile(dp_boots_norm, 2.5))
        ci_high.append(np.percentile(dp_boots_norm, 97.5))
        colors_list.append(COLORS[pn])

    y_pos = np.arange(len(labels))
    ax.errorbar(centers, y_pos,
                xerr=[np.array(centers) - np.array(ci_low), np.array(ci_high) - np.array(centers)],
                fmt="o", capsize=4, markersize=8, color="black")
    for i, (c, lo, hi, col) in enumerate(zip(centers, ci_low, ci_high, colors_list)):
        ax.plot(c, i, "o", markersize=10, color=col)
        ax.text(hi + 0.02, i, f"{c:.3f} [{lo:.3f}, {hi:.3f}]", va="center", fontsize=8)

    ax.set_yticks(y_pos)
    ax.set_yticklabels(labels)
    ax.set_xlabel("d-prime (Fisher Discriminant Ratio)")
    ax.set_title("d-prime with Bootstrap 95% CI")
    ax.axvline(3.0, color="gray", ls=":", lw=1, label="d'=3.0 (good separation)")
