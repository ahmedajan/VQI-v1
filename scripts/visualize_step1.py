def plot_bland_altman(scores):
    """Bland-Altman provider agreement plots."""
    print("  1.6_scores: score_bland_altman_providers.png...")
    pairs = [("P1_ECAPA", "P2_RESNET"), ("P1_ECAPA", "P3_ECAPA2"), ("P2_RESNET", "P3_ECAPA2")]

