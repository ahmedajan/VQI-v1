def compute_dprime(gen, imp):
    """Fisher discriminant ratio."""
    return (gen.mean() - imp.mean()) / (gen.std() + imp.std())


def bootstrap_ci(data, stat_func=np.mean, n_boot=10000, ci=0.95, seed=42):
    """Bootstrap CI for a statistic."""
    rng = np.random.RandomState(seed)
    n = len(data)
    stats = np.array([stat_func(rng.choice(data, n, replace=True)) for _ in range(n_boot)])
    lo = np.percentile(stats, (1 - ci) / 2 * 100)
    hi = np.percentile(stats, (1 + ci) / 2 * 100)
    return lo, hi


