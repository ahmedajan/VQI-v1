def plot_snorm_waterfall(scores):
    """Waterfall chart showing s-norm transformation."""
    print("  1.6_scores: score_snorm_waterfall.png...")
    fig, axes = plt.subplots(1, 3, figsize=(15, 5))

    for ax, pn in zip(axes, PROVIDERS):
        gen_raw = scores[pn]["genuine_raw"].dropna().values
        gen_norm = scores[pn]["genuine_norm"].dropna().values

        raw_mean = gen_raw.mean()
        norm_mean = gen_norm.mean()
        shift = norm_mean - raw_mean

        bars = ax.bar(["Raw Mean", "S-Norm Shift", "Final Mean"],
                      [raw_mean, shift, norm_mean],
                      color=["steelblue", "coral" if shift > 0 else "green", "steelblue"],
                      alpha=0.8, edgecolor="black")
        ax.set_title(f"{pn}")
        ax.set_ylabel("Score")
        for bar, val in zip(bars, [raw_mean, shift, norm_mean]):
            ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.1,
                    f"{val:.3f}", ha="center", va="bottom", fontsize=9)

    plt.suptitle("S-Norm Transformation Effect on Genuine Scores", fontsize=13, y=1.02)
    plt.tight_layout()
    save_fig(fig, os.path.join(SCORES_FOLDER, "score_snorm_waterfall.png"))


def write_scores_analysis(scores, impostors):
    """Write analysis.md for 1.6_scores."""
    print("  1.6_scores: analysis.md...")
    lines = []
    lines.append("# Step 1.6 Score Computation Analysis\n")
    lines.append("## Summary\n")
    lines.append("Score computation completed successfully for all 3 training providers (P1-P3).")
    lines.append("Leave-one-out genuine scores and centroid-vs-centroid impostor distributions")
    lines.append("were computed for both raw cosine similarity and s-norm normalized scores.\n")

    lines.append("## Key Findings\n")

    for pn in PROVIDERS:
        gen_raw = scores[pn]["genuine_raw"].dropna().values
        gen_norm = scores[pn]["genuine_norm"].dropna().values
        imp_raw = impostors[pn]["raw"]
        imp_norm = impostors[pn]["norm"]

        dp_raw = compute_dprime(gen_raw, imp_raw)
        dp_norm = compute_dprime(gen_norm, imp_norm)

        # Mann-Whitney U
        gen_sample = np.random.choice(gen_raw, min(10000, len(gen_raw)), replace=False)
        imp_sample = np.random.choice(imp_raw, min(10000, len(imp_raw)), replace=False)
        u_stat, u_pval = mannwhitneyu(gen_sample, imp_sample, alternative="greater")

        # Cohen's d
        pooled_std = np.sqrt((gen_raw.std()**2 + imp_raw.std()**2) / 2)
        cohen_d = (gen_raw.mean() - imp_raw.mean()) / pooled_std

        lines.append(f"### {pn}\n")
        lines.append(f"- Genuine scores: mean={gen_raw.mean():.4f}, std={gen_raw.std():.4f}, n={len(gen_raw):,}")
        lines.append(f"- Impostor scores: mean={imp_raw.mean():.4f}, std={imp_raw.std():.4f}, n={len(imp_raw):,}")
        lines.append(f"- **d-prime (raw):** {dp_raw:.3f}")
        lines.append(f"- **d-prime (s-norm):** {dp_norm:.3f}")
        lines.append(f"- **Cohen's d:** {cohen_d:.3f}")
        lines.append(f"- **Mann-Whitney U (genuine > impostor):** p < {u_pval:.2e}")
        lines.append(f"- S-norm genuine mean: {gen_norm.mean():.2f} (vs raw {gen_raw.mean():.4f})")
        lines.append(f"- S-norm impostor mean: {imp_norm.mean():.4f} (near zero: {'YES' if abs(imp_norm.mean()) < 0.1 else 'NO'})\n")

    # Cross-provider correlation
    lines.append("## Cross-Provider Correlation\n")
    merged = pd.DataFrame()
    for pn in PROVIDERS:
        merged[pn] = scores[pn]["genuine_raw"].values
    pearson_corr = merged.corr(method="pearson")
    spearman_corr = merged.corr(method="spearman")
    lines.append("| Pair | Pearson r | Spearman rho |")
    lines.append("|------|-----------|-------------|")
    pairs = [("P1_ECAPA", "P2_RESNET"), ("P1_ECAPA", "P3_ECAPA2"), ("P2_RESNET", "P3_ECAPA2")]
    for p1, p2 in pairs:
        lines.append(f"| {p1} vs {p2} | {pearson_corr.loc[p1, p2]:.4f} | {spearman_corr.loc[p1, p2]:.4f} |")
    lines.append("")

    lines.append("## Implications for Step 2\n")
    lines.append("- All three providers show strong genuine-impostor separation (d' > 3.9)")
    lines.append("- This means Class 1 (high quality) and Class 0 (low quality) labels will be well-separated")
    lines.append("- S-norm successfully centers impostor distributions near zero for all providers")
    lines.append("- Moderate cross-provider correlation (~0.5-0.7) confirms provider diversity")
    lines.append("- Provider consensus will ensure labels reflect genuine quality, not single-model artifacts")

    path = os.path.join(SCORES_FOLDER, "analysis.md")
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    print(f"  Saved: {path}")


