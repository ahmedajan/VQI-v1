    for ax, (pn1, pn2) in zip(axes, pairs):
        s1 = scores[pn1]["genuine_raw"].dropna().values
        s2 = scores[pn2]["genuine_raw"].dropna().values
        n = min(len(s1), len(s2))
        # Subsample for plotting
        idx = np.random.choice(n, min(50000, n), replace=False)
        mean_s = (s1[idx] + s2[idx]) / 2
        diff_s = s1[idx] - s2[idx]

        ax.scatter(mean_s, diff_s, s=1, alpha=0.05, c="steelblue")
        mean_diff = diff_s.mean()
        std_diff = diff_s.std()
        ax.axhline(mean_diff, color="red", ls="-", lw=1.5, label=f"Mean diff={mean_diff:.4f}")
        ax.axhline(mean_diff + 1.96 * std_diff, color="red", ls="--", lw=1,
                    label=f"+1.96 SD={mean_diff + 1.96 * std_diff:.3f}")
        ax.axhline(mean_diff - 1.96 * std_diff, color="red", ls="--", lw=1,
                    label=f"-1.96 SD={mean_diff - 1.96 * std_diff:.3f}")
        ax.set_xlabel(f"Mean({pn1}, {pn2})")
        ax.set_ylabel(f"{pn1} - {pn2}")
        ax.set_title(f"Bland-Altman: {pn1} vs {pn2}")
