        imp_norm = impostors[pn]["norm"]

        dp_raw = compute_dprime(gen_raw, imp_raw)
        dp_norm = compute_dprime(gen_norm, imp_norm)

        # Bootstrap CI for d-prime (raw)
        def dprime_boot(gen_s, imp_s):
            def func(data):
                return compute_dprime(data[:len(gen_s)], data[len(gen_s):])
