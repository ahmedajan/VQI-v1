def plot_score_ridgeline(scores, impostors):
    """Ridgeline-style plot of score distributions."""
    print("  1.6_scores: score_ridgeline_by_provider.png...")
    fig, ax = plt.subplots(figsize=(12, 8))
    x_range = np.linspace(-0.5, 1.0, 500)
    offset = 0
    spacing = 1.5

    for pn in PROVIDERS:
        gen = scores[pn]["genuine_raw"].dropna().values
        imp = impostors[pn]["raw"]
        imp_s = np.random.choice(imp, min(200000, len(imp)), replace=False)

        kde_gen = gaussian_kde(gen)(x_range)
        kde_imp = gaussian_kde(imp_s)(x_range)

        # Normalize for visibility
        kde_gen = kde_gen / kde_gen.max() * 0.8
        kde_imp = kde_imp / kde_imp.max() * 0.8

        ax.fill_between(x_range, offset, offset + kde_gen, alpha=0.5, color="green")
        ax.plot(x_range, offset + kde_gen, color="green", lw=1.5)
        ax.fill_between(x_range, offset, offset + kde_imp, alpha=0.3, color="red")
        ax.plot(x_range, offset + kde_imp, color="red", lw=1.5)
        ax.text(-0.55, offset + 0.3, pn, fontsize=10, fontweight="bold", va="center")
        offset += spacing

    ax.set_xlabel("Cosine similarity")
    ax.set_ylabel("")
    ax.set_yticks([])
    ax.set_title("Score Distributions (Green=Genuine, Red=Impostor)")
    # Legend
    from matplotlib.patches import Patch
